package $01_입출력과_사칙연산;

import java.util.Scanner;

public class $09_사칙연산_10869_브론즈5 {
	
	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		int A = sc.nextInt();
		int B = sc.nextInt();
		
		System.out.println(A + B);
		System.out.println(A - B);
		System.out.println(A * B);
		System.out.println(A / B);
		System.out.println(A % B);
		
		sc.close();
		
	}

}
